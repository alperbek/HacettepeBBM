		ORG 0200H

UMODEREG	EQU 086H
UPORT1CTL	EQU 088H
UPORT1		EQU 090H
UPORT2		EQU 092H
		
		MOV AL, 40H
		OUT UPORT1CTL, AL
	
		MOV AL, 03H
		OUT UMODEREG, AL
		MOV AL, 40H
		OUT UPORT1, AL
		

DETECT:		IN AL, UPORT1
		TEST AL, 80H
		JZ ALARM

		MOV AL, 00H
		OUT UPORT2, AL
		JMP DETECT

ALARM:		MOV AL, 0FFH
		OUT UPORT2, AL
		JMP DETECT

END